package br.com.salaDeAula;

public class Funcionario {

    int matricula;
    String nome;
    String telefone;
    String email;

    public Funcionario(int matricula, String nome, String telefone, String email) {
        this.matricula = matricula;
        this.nome = nome;
        this.telefone = telefone;
        this.email = email;
    }
}
